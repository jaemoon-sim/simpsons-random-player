const randomGUID = () => {
    var season = Math.floor(Math.random() * simpsons.length)
    var episode = Math.floor(Math.random() * Object.keys(simpsons[season]).length)

    var guid = ""
    var idx = 0;
    for (var prop in simpsons[season]) {
        if (idx == episode) {
            guid = simpsons[season][prop];
            break;
        }
        idx++;
    }

    
    return guid
}

const attachMutationObserver = () => {

    if (document_observer) {
        return;
    }
    document_observer = new MutationObserver(() => {
        const media = document.querySelectorAll(".play");
        if (media.length > 0) {
            if (!playlock) {
                playlock = true
                var guid = randomGUID()
                playVideo(guid);
            }
        }
    });
    document_observer.observe(document.body, {
        attributes: false,
        childList: true,
        subtree: true
    });
}

window.addEventListener("DOMContentLoaded", () => {
    attachMutationObserver();
})